// ForEach

// Es un método que se usa en los arrays que nos permite recorrer todos los elementos del array uno por uno

// Es decir, cuando quieres usar cada elemento del array, pero no transformar el array

// - Si quisieras transformar el array => map()
// - Si quieres filtrar elementos => filter()
// - Si quisieras buscar uno solo => find()

// Estructura del forEach

array.forEach(element, indice => {
    // codigo a ejecutar
});

//array.forEach(...) => El método que se ejecuta sobre el array

// (elemento) => El elemento actual del array en esa vuelta

// Si añades segundo parámetro, obtienes el indice